/**
 * ابر — المان اصلی هویت بصری برند.
 *
 * === چرا اجتماع دایره‌ها و نه زنجیرهٔ کمان‌ها ===
 * نسخه‌های قبلی از زنجیره‌ای از کمان‌ها ساخته می‌شدند که در نقاط داده‌شده به هم
 * می‌رسیدند. مشکل: مماسِ دو کمانِ مجاور در آن نقطه یکی نبود — در گوشهٔ چپ ابر
 * اختلافشان به ۴۵ درجه می‌رسید و آنجا یک **نوکِ محدب** می‌ساخت، نه گوشهٔ گِرد.
 * با تنظیم ضرایب هم درست نمی‌شد؛ ایراد ساختاری بود.
 *
 * حالا هر شکل، مرزِ **اجتماع چند دایرهٔ هم‌پوشان** است. در اجتماع دیسک‌ها نوکِ
 * محدب ریاضیاً ممکن نیست: هر نقطهٔ مرزی یا وسط یک کمان است (کاملاً گِرد) یا
 * محل تقاطع دو دایره، که همیشه فرورفتگیِ مقعر است — همان درزی که بین دو پفِ
 * ابر انتظار داریم. پس دور تا دور گِرد است، بدون استثنا.
 *
 * مولد: scratchpad/union.js — دایره‌ها را می‌گیرد و کمان‌های دیدنی را زنجیر
 * می‌کند. اگر خواستی شکل را عوض کنی، دایره‌ها را آنجا تغییر بده و دوباره
 * تولید کن؛ مسیرها را دستی ویرایش نکن.
 *
 * رنگ از currentColor می‌آید تا با کلاس‌های Tailwind قابل تغییر باشد.
 */

export type CloudShape = "main" | "chubby" | "long";

const SHAPES: Record<CloudShape, { viewBox: string; d: string }> = {
  /** اجتماع ۷ دایره، نسبت ۱٫۸۸. نشان برند؛ همه‌جای سایت همین است. */
  main: {
    viewBox: "6 11 220 118.9",
    d:
      "M68.3 49.8 A42 42 0 0 1 147.8 36.6 A33 33 0 0 1 190.9 65.8 A24 24 0 0 1 224 88 " +
      "A24 24 0 0 1 177 95 A33 33 0 0 1 134.6 91.3 A28 28 0 0 1 136 100 " +
      "A28 28 0 0 1 88.8 120.3 A24 24 0 0 1 53.2 107.6 A30 30 0 0 1 43.7 104.4 " +
      "A20 20 0 1 1 28.6 72 A30 30 0 0 1 68.3 49.8 Z",
  },

  /** اجتماع ۵ دایره، نسبت ۱٫۳۷ — کوتاه و تپل. فقط پس‌زمینه. */
  chubby: {
    viewBox: "12.1 12 143.9 106",
    d:
      "M46.4 46.4 A38 38 0 0 1 119.5 38.3 A30 30 0 0 1 154 68 A30 30 0 0 1 127.7 97.8 " +
      "A22 22 0 0 1 85.6 102.4 A24 24 0 0 1 42.2 102 A28 28 0 1 1 46.4 46.4 Z",
  },

  /** اجتماع ۶ دایره، نسبت ۲٫۰۷ — پهن و کم‌ارتفاع. فقط پس‌زمینه. */
  long: {
    viewBox: "18 26 190 93.9",
    d:
      "M60.2 56.2 A32 32 0 0 1 119.7 43.9 A30 30 0 0 1 170 65 A22 22 0 0 1 206 82 " +
      "A22 22 0 0 1 162.4 86 A30 30 0 0 1 134 95.4 A24 24 0 0 1 88.8 105.3 " +
      "A22 22 0 0 1 50.2 103.7 A26 26 0 1 1 60.2 56.2 Z",
  },
};

export function Cloud({
  shape = "main",
  className,
  style,
}: {
  shape?: CloudShape;
  className?: string;
  /** برای جای‌گذاری و پارامترهای انیمیشن */
  style?: React.CSSProperties;
}) {
  const { viewBox, d } = SHAPES[shape];

  return (
    <svg
      viewBox={viewBox}
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      style={style}
      aria-hidden="true"
      focusable="false"
    >
      {/*
       * تک‌رنگ و تخت، بدون سایه‌روشن.
       * سه تلاش برای سایهٔ سفید هر بار بدتر شد: گرادیان شعاعی مثل حباب دیده
       * می‌شد، دایرهٔ بزرگ مرزی تقریباً صاف می‌ساخت، و سیلوئتِ جابه‌جاشده مثل
       * ابرِ دومی روی ابر اول می‌نشست. فرمِ خودِ ابر به‌قدر کافی گویاست و
       * تخت بودن با زبان بقیهٔ سایت هم جور است.
       */}
      <path fill="currentColor" d={d} />
    </svg>
  );
}

/**
 * نشان برند: ابر کوچک + نام استودیو.
 */
export function Logo({ className = "" }: { className?: string }) {
  return (
    <span className={`inline-flex items-center gap-2.5 ${className}`}>
      <Cloud className="w-12 shrink-0 text-sky-deep" />
      <span className="text-[0.98rem] font-semibold tracking-tight text-ink">
        ابر سفید
      </span>
    </span>
  );
}
