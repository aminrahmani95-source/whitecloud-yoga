import { contact } from "@/lib/data/contact";

/**
 * شمارهٔ تلفن استودیو — یک‌تکه و در جهت درست.
 *
 * شماره در چند جای سایت تکرار می‌شود و هر بار دو تله سر راهش است. این
 * کامپوننت هست تا هر دو تله یک‌بار و در یک جا بسته شوند، نه اینکه هر صفحه
 * خودش یادش باشد.
 *
 * ── تلهٔ اول: شکستن وسط شماره ──────────────────────────────────────────
 * پیش از این پیوند شماره white-space: normal داشت، یعنی مرورگر مجاز بود
 * «۰۹۳۸ ۷۷ ۷۷ ۳۳۷» را سر هر فاصله بشکند و نیمی از شماره را به سطر بعد
 * ببرد. حتی وقتی نمی‌شکست هم چون آخر سطر می‌افتاد، خواننده گمان می‌کرد
 * شکسته است. whitespace-nowrap این را قطعی می‌بندد.
 *
 * ── تلهٔ دوم: وارونه شدن شماره ─────────────────────────────────────────
 * ارقام فارسی کلاس دوجهتهٔ EN دارند ولی فاصلهٔ بین گروه‌ها خنثی است و در
 * جملهٔ راست‌به‌چپ به جهت پاراگراف می‌افتد. نتیجه‌اش این است که بدون
 * جداسازی، شماره به هفت قطعهٔ جدا تکه می‌شود و «۳۳۷ ۷۷ ۷۷ ۰۹۳۸» خوانده
 * می‌شود — اندازه‌گیری شده، نه فرضی.
 *
 * پس bdi و dir="ltr" هر دو لازم‌اند و هیچ‌کدام تزئینی نیست:
 *   • bdi شماره را از الگوریتم دوجهتهٔ جملهٔ اطرافش جدا می‌کند (isolate)
 *   • dir="ltr" جهت داخل آن را صریح می‌کند؛ روی رقم تنها، dir="auto"
 *     تکیه‌گاه قوی پیدا نمی‌کند و نتیجه‌اش به بافت وابسته می‌شود
 * هیچ‌کدام را بدون دیگری برندارید.
 */
export function PhoneNumber({ className = "" }: { className?: string }) {
  return (
    <bdi dir="ltr" className={`tabular whitespace-nowrap ${className}`}>
      {contact.phone.display}
    </bdi>
  );
}

/**
 * همان شماره، در نقش پیوند tel — برای وقتی که شماره خودش کلیک‌شدنی است.
 *
 * py-1.5 ارتفاع هدف لمسی را از ۲۴٫۵ به ۳۶٫۵ پیکسل می‌برد (WCAG 2.5.8 روی
 * ۲۴ اصرار دارد، ولی ۲۴ برای انگشت شست تنگ است).
 *
 * این padding عمداً با margin منفی خنثی نشده: در فهرست تماسِ فوتر، پیوندها
 * پشت سر هم می‌آیند و اگر هرکدام سطح لمسش را به بیرون بکشد، سطح‌ها روی هم
 * می‌افتند و لمس نزدیک مرز، پیوند اشتباه را باز می‌کند. جایی که پاراگرافِ
 * دورش نباید تکان بخورد — یعنی وقتی شماره وسط جملهٔ فارسی نشسته — خودِ
 * فراخواننده «-my-1.5» را اضافه می‌کند.
 */
export function PhoneLink({ className = "" }: { className?: string }) {
  return (
    <a
      href={contact.phone.href}
      className={`inline-block py-1.5 align-baseline ${className}`}
    >
      <PhoneNumber />
    </a>
  );
}
