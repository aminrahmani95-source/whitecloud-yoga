/**
 * پوسته و تیتر مشترک بخش‌ها.
 *
 * همهٔ بخش‌های سایت از این دو استفاده می‌کنند تا فاصله‌ها، عرض ستون و
 * سلسله‌مراتب تیترها یکدست بماند و هر بخش سلیقهٔ خودش را اعمال نکند.
 *
 * ══════════════════════════════════════════════════════════════════════
 *  نردبان فاصله — پیش از افزودن هر عدد جدید، این را بخوانید
 * ══════════════════════════════════════════════════════════════════════
 * فاصله‌های عمودی سایت روی پنج پله می‌نشینند و نه بیشتر. پیش از این
 * سیزده مقدار مختلف در کد بود (mt-3 تا mt-24، gap-1 تا gap-20) که هیچ‌کدام
 * از دیگری تبعیت نمی‌کرد؛ نتیجه‌اش این بود که دو بخشِ کنار هم بی‌دلیل
 * ریتم متفاوت داشتند.
 *
 *   پله ۱ — درون یک بلوک           mt-3 (تیتر ← متن، آیتم ← آیتم)
 *   پله ۲ — بین بلوک‌های یک کارت    mt-6
 *   پله ۳ — بین تیتر بخش و محتوا    mb-10 sm:mb-12   ← SectionHeading
 *   پله ۴ — بین کارت‌ها (gap)        gap-6  کارت‌ها
 *                                   gap-10 ستون‌های بزرگ
 *   پله ۵ — بین بخش‌ها              py-20 sm:py-24   ← همین Section
 *
 * قاعده: برای فاصلهٔ عمودیِ **بخش‌های محتوا** عددی خارج از این پله‌ها
 * ننویسید. اگر جایی واقعاً به فاصلهٔ دیگری نیاز داشت، یعنی یا پله‌ای کم
 * است (اینجا اضافه‌اش کنید و مستندش کنید) یا آن بخش باید ساختارش عوض شود.
 *
 * ── چیزهایی که عمداً بیرون این نردبان‌اند ─────────────────────────────
 * این‌ها تخطی نیستند و «اصلاح»شان کار را بدتر می‌کند:
 *
 *  • نوارهای قاب صفحه — سربرگ PageShell (py-14 sm:py-16)، فوتر (py-14) و
 *    صحنهٔ ورودی (py-16). این‌ها بخشِ محتوا نیستند؛ قاب دور محتوا هستند و
 *    عمداً کمی جمع‌تر از پلهٔ پنجم‌اند تا محتوا برجسته بماند.
 *
 *  • بزرگ‌شدن پلکانی فاصله در بریک‌پوینت‌های بالاتر — مثل
 *    md:gap-12 یا lg:gap-14. نردبان مقدارِ پایه را تعیین می‌کند؛ رشدش با
 *    عرض صفحه طبیعی است و نباید ثابت بماند.
 *
 *  • padding داخل دکمه، بج و پیوند (py-1.5 تا py-3.5). این‌ها اندازهٔ خودِ
 *    عنصرند، نه فاصلهٔ بین عناصر. سطح لمس روی موبایل هم به همین‌ها بند است.
 *
 *  • className="pt-0" وقتی دو Section پشت سر هم می‌آیند و padding پایینیِ
 *    اولی با بالاییِ دومی جمع می‌شود.
 *
 * ── عرض ستون متن ──────────────────────────────────────────────────────
 * برای متن پیوسته از utility «measure» استفاده کنید (globals.css) نه
 * max-w-* دلبخواه. ستون پهن‌تر از آن، سطرها را آن‌قدر بلند می‌کند که چشم
 * موقع برگشتن سطر را گم کند.
 */

export function Section({
  id,
  className = "",
  children,
}: {
  id?: string;
  className?: string;
  children: React.ReactNode;
}) {
  return (
    <section
      id={id}
      className={`mx-auto max-w-6xl px-5 py-20 sm:px-8 sm:py-24 ${className}`}
    >
      {children}
    </section>
  );
}

export function SectionHeading({
  eyebrow,
  title,
  lead,
}: {
  eyebrow?: string;
  title: string;
  /**
   * ReactNode است و نه string: بعضی لیدها شمارهٔ تلفن دارند و شماره باید
   * کامپوننت باشد تا جداسازی دوجهته و nowrap خودش را همراه بیاورد.
   */
  lead?: React.ReactNode;
}) {
  return (
    <div className="mb-10 max-w-2xl sm:mb-12">
      {eyebrow && (
        <p className="mb-3 text-sm font-medium tracking-wide text-sky-text">
          {eyebrow}
        </p>
      )}
      <h2 className="text-2xl font-semibold leading-snug text-ink sm:text-3xl">
        {title}
      </h2>
      {lead && (
        <p className="measure mt-3 text-base leading-loose text-muted sm:text-lg">
          {lead}
        </p>
      )}
    </div>
  );
}
